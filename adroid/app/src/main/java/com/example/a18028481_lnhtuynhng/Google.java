package com.example.a18028481_lnhtuynhng;

public class Google {
    private String textGoogle;
    private String txt90;
    private int imgGoogle;

    public Google(String textGoogle, String txt90, int imgGoogle) {
        this.textGoogle = textGoogle;
        this.txt90 = txt90;
        this.imgGoogle = imgGoogle;
    }

    public String getTextGoogle() {
        return textGoogle;
    }

    public void setTextGoogle(String textGoogle) {
        this.textGoogle = textGoogle;
    }

    public String getTxt90() {
        return txt90;
    }

    public void setTxt90(String txt90) {
        this.txt90 = txt90;
    }

    public int getImgGoogle() {
        return imgGoogle;
    }

    public void setImgGoogle(int imgGoogle) {
        this.imgGoogle = imgGoogle;
    }
}
